    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        int linhaSelecionada = produtosTable.getSelectedRow();
        if (linhaSelecionada < 0) 
            return;
        
        DefaultTableModel modelo = (DefaultTableModel) produtosTable.getModel();
        
        String totalString =  String.valueOf(modelo.getValueAt(linhaSelecionada, 3));
        totalString = totalString.replace(',', '.').replace("R$", "").trim();
        Double total = Double.parseDouble(totalString);
        
        String quantidadeString =  String.valueOf(modelo.getValueAt(linhaSelecionada, 2));
        Integer quantidade = Integer.parseInt(quantidadeString);
        
        quantidadeDeItens -= quantidade;
        totalItemLabel.setText("" + quantidadeDeItens);

        subTotalAPagar -= total;
        subtotalLabel.setText("" + subTotalAPagar);
        
        modelo.removeRow(linhaSelecionada);
        
        produtosTable.setFocusable(false);
    }//GEN-LAST:event_excluirButtonActionPerformed

