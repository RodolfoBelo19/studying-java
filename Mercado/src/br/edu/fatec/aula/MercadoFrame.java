    private void adicionarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarButtonActionPerformed
        // Pegando os dados da tela gráfica
        String produto = produtoTextField.getText();
        Double valorUnitario = Double.parseDouble(valorUnitarioFormattedTextField.getText());
        Integer quantidade = Integer.parseInt(quantidadeTextField.getText());

        // Calculando o total 
        Double total = valorUnitario * quantidade;

        // Adicionar linha na tabela
        DefaultTableModel modelo = (DefaultTableModel) produtosTable.getModel();
        Object[] linha = {produto, valorUnitario, quantidade, total};
        modelo.addRow(linha);

        // Calcular quantidade de itens e subtotal a ser pago
        quantidadeDeItens += quantidade;
        totalItemLabel.setText("" + quantidadeDeItens);

        subTotalAPagar += total;
        subtotalLabel.setText("" + subTotalAPagar);

        // Limpamos a tela
        produtoTextField.setText("");
        valorUnitarioFormattedTextField.setText("");
        quantidadeTextField.setText("");
    }//GEN-LAST:event_adicionarButtonActionPerformed

    private void cancelarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarButtonActionPerformed
        // Limpamos a tela
        produtoTextField.setText("");
        valorUnitarioFormattedTextField.setText("");
        quantidadeTextField.setText("");
        totalItemLabel.setText("0");
        subtotalLabel.setText("R$ 0,00");

        DefaultTableModel modelo = (DefaultTableModel) produtosTable.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }//GEN-LAST:event_cancelarButtonActionPerformed

